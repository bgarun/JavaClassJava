
public class Switch {
	public static void main (String[]args)
	{
		int choice=4;
		switch(choice) {
		case 1:{
			System.out.println("hii");
		}break;
		case 2 :{
			System.out.println("hello");
		}break;
		case 3:{
			System.out.println("good morning");
		}break;
		default : {
			System.out.println("invalid input");
		}
		}
		
	}
}
